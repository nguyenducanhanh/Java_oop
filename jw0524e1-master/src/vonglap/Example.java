package vonglap;

public class Example {
    public static void main(String[] args) {
        // for: lặp đi lặp lại 1 khối lệnh dựa trên 1 điều kiện cho trước
        // việc lặp lại này sẽ tiếp tục đến khi điều kiện sai

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // int i = 0: khai báo bước lặp cho vòng lặp => chỉ làm 1 lần duy nhất
        // điều kiện vòng lặp này: i < 10 => lặp dừng khi điều kiện sai
        // i++: tăng tiến bước lặp => tiến đến điều kiện sai
        // sout(i): khối lệnh thực hiện => thay thế bằng các thể loại lệnh khác: biểu thức, clđk, tạo đối tượng, v.v

    }
}
